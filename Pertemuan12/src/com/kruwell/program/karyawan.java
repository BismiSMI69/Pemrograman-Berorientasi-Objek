    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
        // Validasi input tidak boleh kosong
            if (
                jTextField1.getText().trim().isEmpty() ||
                jTextField2.getText().trim().isEmpty() ||
                jComboBox1.getSelectedItem().toString().equals("-- Pilih Jabatan --") ||
                jTextField3.getText().trim().isEmpty() ||
                jTextField4.getText().trim().isEmpty() ||
                jTextField5.getText().trim().isEmpty()
            ) {
                JOptionPane.showMessageDialog(this, "Gagal menyimpan data. Semua field wajib diisi!");
                return; // hentikan proses simpan
            }


            // Jika semua field terisi, lakukan simpan
            String sql = "INSERT INTO karyawan (nik, nama, jabatan, alamat, email, no_telp) VALUES (?, ?, ?, ?, ?, ?)";
            java.sql.Connection conn = (java.sql.Connection) config.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);

            pst.setString(1, jTextField1.getText());  
            pst.setString(2, jTextField2.getText());   
            pst.setString(3, jComboBox1.getSelectedItem().toString()); 
            pst.setString(4, jTextField3.getText());     
            pst.setString(5, jTextField4.getText());
            pst.setString(6, jTextField5.getText()); 

            pst.execute();
            JOptionPane.showMessageDialog(this, "Penyimpanan Data Berhasil");

            load_table();
            kosong(); // kosongkan form setelah berhasil simpan

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

