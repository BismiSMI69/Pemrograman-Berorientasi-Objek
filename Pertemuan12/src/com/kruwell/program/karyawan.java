        public void searchByNama(String nama) {
            try {
                String sql = "SELECT * FROM karyawan WHERE nama LIKE ?";
                java.sql.Connection conn = (java.sql.Connection) config.configDB();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, "%" + nama + "%"); // pakai LIKE agar fleksibel (mirip pencarian)

                java.sql.ResultSet rs = pst.executeQuery();
                DefaultTableModel model = new DefaultTableModel();
                model.addColumn("NIK");
                model.addColumn("Nama");
                model.addColumn("Jabatan");
                model.addColumn("Alamat");
                model.addColumn("Email");
                model.addColumn("No. Telp");

                while (rs.next()) {
                    model.addRow(new Object[]{
                        rs.getString("nik"),
                        rs.getString("nama"),
                        rs.getString("jabatan"),
                        rs.getString("alamat"),
                        rs.getString("email"),
                        rs.getString("no_telp")
                    });
                }

                jTable1.setModel(model); // tampilkan ke tabel

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Pencarian Gagal: " + e.getMessage());
            }
        }

    
        private void load_table() {
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("No");
            model.addColumn("NIK");
            model.addColumn("Nama Karyawan");
            model.addColumn("Jabatan");
            model.addColumn("Alamat");
            model.addColumn("Email");
            model.addColumn("No. Telp");

            try {
                int no = 1;
                String sql = "SELECT * FROM karyawan";
                java.sql.Connection conn = (java.sql.Connection) config.configDB();
                java.sql.Statement stm = conn.createStatement();
                java.sql.ResultSet rs = stm.executeQuery(sql);

                while (rs.next()) {
                    model.addRow(new Object[]{
                        no++,
                        rs.getString("nik"),
                        rs.getString("nama"),
                        rs.getString("jabatan"),
                        rs.getString("alamat"),
                        rs.getString("email"),
                        rs.getString("no_telp")
                    });
                }

                jTable1.setModel(model);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Gagal memuat data: " + e.getMessage());
            }
        }

    
